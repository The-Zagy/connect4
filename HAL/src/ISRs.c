void GPIOPortF_Handler(void)
{
	Nokia5110_ClearBuffer();
	Nokia5110_DisplayBuffer();      // draw buffer
	
	if(GPIOF->GPIORIS & (1<<0))  
	{//s1 pressed
		GPIOF->GPIOICR= 0x01;
		move_right();
	}
	else if (GPIOF->GPIORIS  & (1<<4))
	{//s3 pressed
		GPIOF->GPIOICR = 0x10;
		move_left();
	}
}

